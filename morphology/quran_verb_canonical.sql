CREATE TABLE quran_verb_canonical (
  root_ar            VARCHAR(16) NOT NULL  COMMENT 'Racine en arabe, espaces entre lettres (ex: "ك ت ب")',
  verb_form          TINYINT UNSIGNED NOT NULL DEFAULT 1
